const TCHAR* WanaWorksRetargetRootPath = TEXT("/Game/WanaWorks/Retarget");

FString SanitizeWanaRetargetAssetNameFragment(const FString& InName)
{
    FString Result;
    Result.Reserve(InName.Len());

    for (const TCHAR Character : InName)
    {
        Result.AppendChar(FChar::IsAlnum(Character) ? Character : TEXT('_'));
    }

    return Result.IsEmpty() ? TEXT("Unnamed") : Result;
}

bool SaveWanaRetargetPackage(UPackage* Package, UObject* Asset, const FString& PackagePath)
{
    if (!Package || !Asset)
    {
        return false;
    }

    Package->MarkPackageDirty();

    const FString PackageFilename = FPackageName::LongPackageNameToFilename(
        PackagePath,
        FPackageName::GetAssetPackageExtension());
    IFileManager::Get().MakeDirectory(*FPaths::GetPath(PackageFilename), true);

    FSavePackageArgs SaveArgs;
    SaveArgs.TopLevelFlags = RF_Public | RF_Standalone;
    SaveArgs.SaveFlags = SAVE_NoError;

    return UPackage::SavePackage(Package, Asset, *PackageFilename, SaveArgs);
}

// Finds an existing WanaWorks-owned IK Rig for this mesh or creates one, then re-runs UE's
// auto-characterizer against it so the readiness report always reflects the current mesh.
UIKRigDefinition* FindOrCreateWanaRetargetIKRig(
    USkeletalMesh* Mesh,
    const FString& MeshLabel,
    FAutoCharacterizeResults& OutCharacterizeResults,
    FString& OutIKRigAssetPath)
{
    if (!Mesh)
    {
        return nullptr;
    }

    const FString AssetName = FString::Printf(TEXT("IKRig_%s"), *SanitizeWanaRetargetAssetNameFragment(MeshLabel));
    const FString PackagePath = FString::Printf(TEXT("%s/%s"), WanaWorksRetargetRootPath, *AssetName);
    OutIKRigAssetPath = PackagePath;

    UIKRigDefinition* RigAsset = LoadObject<UIKRigDefinition>(nullptr, *FString::Printf(TEXT("%s.%s"), *PackagePath, *AssetName));
    UPackage* Package = RigAsset ? RigAsset->GetOutermost() : CreatePackage(*PackagePath);

    if (!Package)
    {
        return nullptr;
    }

    const bool bCreatedNewAsset = RigAsset == nullptr;

    if (!RigAsset)
    {
        RigAsset = NewObject<UIKRigDefinition>(Package, UIKRigDefinition::StaticClass(), *AssetName, RF_Public | RF_Standalone | RF_Transactional);
    }

    if (!RigAsset)
    {
        return nullptr;
    }

    UIKRigController* Controller = UIKRigController::GetController(RigAsset);

    if (!Controller)
    {
        return nullptr;
    }

    Controller->SetSkeletalMesh(Mesh);
    Controller->AutoGenerateRetargetDefinition(OutCharacterizeResults);
    Controller->ApplyAutoGeneratedRetargetDefinition();

    if (bCreatedNewAsset)
    {
        FAssetRegistryModule::AssetCreated(RigAsset);
    }

    SaveWanaRetargetPackage(Package, RigAsset, PackagePath);

    return RigAsset;
}

FWanaAutoRetargetSideResult BuildSideResult(
    const FString& MeshLabel,
    const FString& IKRigAssetPath,
    const FAutoCharacterizeResults& CharacterizeResults,
    int32 DetectedChainCount)
{
    FWanaAutoRetargetSideResult SideResult;
    SideResult.SkeletalMeshLabel = MeshLabel;
    SideResult.IKRigAssetPath = IKRigAssetPath;
    SideResult.bUsedKnownTemplate = CharacterizeResults.bUsedTemplate;
    SideResult.MatchedTemplateName = CharacterizeResults.BestTemplateName.IsNone()
        ? TEXT("(no known template match)")
        : CharacterizeResults.BestTemplateName.ToString();
    SideResult.TemplateMatchScore = CharacterizeResults.BestPercentageOfTemplateScore;
    SideResult.DetectedChainCount = DetectedChainCount;
    SideResult.MissingBoneCount = CharacterizeResults.MissingBones.Num();
    return SideResult;
}
